#include <iostream>
using namespace std;


int main () {

  int i = 7 ;
   cout << (--i) << endl; // 7 bata ghatyo 6
   cout << (i++) << endl; // 6 nai thyo ani balla 7 
   cout << (i--) << endl; // 7 nai theyo ani balla 6
   cout << (++i) << endl; // 6 bata 7 vayo 

  return 0;

}                             