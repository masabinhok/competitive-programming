#include <bits/stdc++.h>

using namespace std;


// if the algorithm contains, consecutive phases, the total time complexity is the largest time complexity among the phases, because usually the slowest phase dominates the total time complexity.

int main ( ){
  //time complexity O(n)
  int n;
  for(int i =0 ; i<=n; i++){
    //code
  }
//time complexity O(n^2)

  for(int i =0 ; i<=n; i++){
    for(int j =0 ; j<=n; j++){
      //code
    }
  }
  //time complexity O(n)

  for(int i =0 ; i<=n; i++){
    //code
  }
}


//total time complexity is O(n^2) as the slowest phase dominates the total time complexity,largest time complexity among the phases.